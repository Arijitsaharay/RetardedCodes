main() {
  // var declares a variable and dart analyzer infers the type
  var a = "dart";
  print(a);

  // num is a type
  num b = 42;
  print(b);

  // final variable cannot be changed once declared
  final num c = 7;
  print(c);

  // compile time constant
  const double d = 7.777;
  print(d);
}
