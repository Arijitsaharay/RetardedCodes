main() {
  // if-else statement
  if (7 % 2 == 0) {
    print('7 is even number');
  } else {
    print('7 is odd');
  }

  // if statement
  if (8 % 4 == 0) {
    print('8 is divisble by 4');
  }

  // ternary operator
  var isAlive = true;
  // ignore: dead_code
  var monday = isAlive ? 'doctor' : 'null';
  print('monday appointment: $monday');
}
